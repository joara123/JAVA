package week3;

public class Word {
	private String Eword;
	private String Kword;
	
	public Word(String Eword, String Kword) {
		this.Eword = Eword;
		this.Kword = Kword;
	}

	public String getEword() {
		return Eword;
	}

	public String getKword() {
		return Kword;
	}
	
	public String toString() {
		return "dd";
	}
}
